alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']

def conversion(direction,text,shift):
    cipher_text =""
    for i in text:
        if direction == 'encode':
            n = alphabet.index(i) + shift
        else:
            n = alphabet.index(i) - shift
        while n > 25:
            n -=26
        cipher_text+=alphabet[n]
    print("converted Text : ",cipher_text)

